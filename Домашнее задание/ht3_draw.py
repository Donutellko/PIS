def task3(w, h, f):
    fw = min(f, w // 2)
    fh = min(f, h // 2)
    printHorizontal(w, fh)
    for i in range(h - 2 * fh):
        print(fw * '*' + (w - 2 * fw) * ' ' + fw * '*')
    printHorizontal(w, fh)


def printHorizontal(w, fh):
    for i in range(fh):
        print(w * '*')


